# Faça um algoritmo que receba três números e exiba a média aritmética.

number01 = float(input("Number 01: "))
number02 = float(input("Number 02: "))
number03 = float(input("Number 03: "))

average = (number01 + number02 + number03) / 3

print(f"The average of number 01, number 02 and number 03 is : {average:.2f}")